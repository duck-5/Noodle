export function getLanguage(): 'he' | 'en' {
  const lang = getPreference('language');
  return (lang === 'en' || lang === 'he') ? lang : 'he'; // Default to Hebrew
}

export function translate(key: keyof typeof translations['en'], params?: Record<string, string | number>): string {
  const lang = getLanguage();
  let text = translations[lang]?.[key] || translations['en']?.[key] || String(key);
  if (params) {
    Object.entries(params).forEach(([k, v]) => {
      text = text.replace(`{${k}}`, String(v));
    });
  }
  return text;
}

